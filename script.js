var currentQuiz = [];
var currentQuestionIndex = 0;
var correctAnswers = 0;
var wrongAnswers = 0;

function startQuiz(category) {
  currentQuiz = quizData[category];
  currentQuestionIndex = 0;
  correctAnswers = 0;
  wrongAnswers = 0;
  
  showQuestion();
  
  document.getElementById("quiz").style.display = "block";
  document.getElementById("result").style.display = "none";
}
function showQuestion() {
  var questionElement = document.getElementById("question");
  var optionsElement = document.getElementById("options");
  
  var currentQuestion = currentQuiz[currentQuestionIndex];
  
  questionElement.innerHTML = currentQuestion.question;
  
  optionsElement.innerHTML = "";
  for (var i = 0; i < currentQuestion.options.length; i++) {
    var option = document.createElement("div");
    option.classList.add("option");
    
    var radioBtn = document.createElement("input");
    radioBtn.type = "radio";
    radioBtn.name = "answer";
    radioBtn.value = i + 1;
    option.appendChild(radioBtn);
    
    var label = document.createElement("label");
    label.innerHTML = currentQuestion.options[i];
    option.appendChild(label);
    
    optionsElement.appendChild(option);
  }
}

function checkAnswer() {
  var selectedAnswer = document.querySelector('input[name="answer"]:checked');
  
  if (selectedAnswer) {
    var answer = parseInt(selectedAnswer.value);
    var currentQuestion = currentQuiz[currentQuestionIndex];
    
    var options = document.getElementsByClassName("option");
    for (var i = 0; i < options.length; i++) {
      options[i].classList.remove("correct-answer");
      options[i].classList.remove("wrong-answer");
    }
    
    if (answer === currentQuestion.answer) {
      selectedAnswer.parentNode.classList.add("correct-answer");
      correctAnswers++;
    } else {
      selectedAnswer.parentNode.classList.add("wrong-answer");
      showCorrectAnswer();
      wrongAnswers++;
    }
    
    document.getElementById("correct").innerHTML = correctAnswers;
    document.getElementById("wrong").innerHTML = wrongAnswers;
    
    disableOptions();
  }
}

function showCorrectAnswer() {
  var currentQuestion = currentQuiz[currentQuestionIndex];
  var options = document.getElementsByClassName("option");
  
  for (var i = 0; i < options.length; i++) {
    if (parseInt(options[i].querySelector("input").value) === currentQuestion.answer) {
      options[i].classList.add("correct-answer");
    }
  }
}

function disableOptions() {
  var options = document.getElementsByTagName("input");
  
  for (var i = 0; i < options.length; i++) {
    options[i].disabled = true;
  }
  
  document.getElementById("submit").disabled = true;
}

function nextQuestion() {
  var selectedAnswer = document.querySelector('input[name="answer"]:checked');
  
  if (selectedAnswer) {
    selectedAnswer.parentNode.classList.remove("correct-answer");
    selectedAnswer.parentNode.classList.remove("wrong-answer");
    selectedAnswer.checked = false;
    selectedAnswer.disabled = false;
  }
  
  document.getElementById("submit").disabled = false;
  
  currentQuestionIndex++;
  
  if (currentQuestionIndex < currentQuiz.length) {
    showQuestion();
  } else {
    showResult();
  }
}
function previousQuestion() {
  currentQuestionIndex--;
  if (currentQuestionIndex >= 0) {
    showQuestion();
  } 
}

function showResult() {
  document.getElementById("quiz").style.display = "none";
  document.getElementById("result").style.display = "block";
  
  document.getElementById("correct").innerHTML = correctAnswers;
  document.getElementById("wrong").innerHTML = wrongAnswers;
}

function restartQuiz() {
  document.getElementById("quiz").style.display = "none";
  document.getElementById("result").style.display = "none";
}